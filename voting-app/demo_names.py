def _load_from_sqlite(db_path):
    """Try to load names from a SQLite database at db_path."""
    names = []
    try:
        conn = sqlite3.connect(db_path)
        try:
            # Try members table first, then attendees
            for table in ("members", "attendees"):
                try:
                    # Try full_name column first, then name
                    for col in ("full_name", "name"):
                        try:
                            cursor = conn.execute(
                                f"SELECT {col} FROM {table}"  # noqa: S608
                            )
                            for row in cursor:
                                val = row[0]
                                if val and str(val).strip():
                                    names.append(str(val).strip())
                            if names:
                                return names
                        except sqlite3.OperationalError:
                            continue
                except sqlite3.OperationalError:
                    continue
        finally:
            conn.close()
    except (sqlite3.Error, OSError):
        pass
    return names


def _load_from_csv(csv_path):
    """Load names from a CSV file with a name or full_name column."""
    names = []
    try:
        with open(csv_path, newline="", encoding="utf-8-sig") as fh:
            reader = csv.DictReader(fh)
            if reader.fieldnames is None:
                return names
            # Determine which column to use
            col = None
            for candidate in ("full_name", "name"):
                if candidate in reader.fieldnames:
                    col = candidate
                    break
            if col is None:
                return names
            for row in reader:
                val = row.get(col, "").strip()
                if val:
                    names.append(val)
    except (OSError, csv.Error):
        pass
    return names


def _load_from_txt(txt_path):
    """Load names from a plain-text file, one name per line."""
    names = []
    try:
        with open(txt_path, encoding="utf-8") as fh:
            for line in fh:
                line = line.strip()
                if line and not line.startswith("#"):
                    names.append(line)
    except OSError:
        pass
    return names

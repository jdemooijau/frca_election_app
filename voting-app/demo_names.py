def generate_demo_names(count=8, member_names=None):
    """Generate demo candidate names.

    If member_names is provided and contains at least 4 entries, names are
    generated by mashing up surname components.  Otherwise the fixed fallback
    lists are returned.

    Args:
        count: Number of names to generate (only used in mash-up mode).
        member_names: Optional list of "First Last" strings from the member DB.

    Returns:
        List of name strings.
    """
    if member_names and len(member_names) >= 4:
        return _generate_names_from_pool(member_names, count=count)

    # Fallback: return the static lists (combined)
    fallback = FALLBACK_ELDER_CANDIDATES + FALLBACK_DEACON_CANDIDATES
    return fallback


def load_member_names_from_db(db_path):
    """Load member names from the app's SQLite database.

    Expects a `members` table with `first_name` and `last_name` columns.

    Args:
        db_path: Absolute path to the SQLite database file.

    Returns:
        List of "first last" name strings.
    """
    names = []
    try:
        conn = sqlite3.connect(db_path)
        try:
            cursor = conn.execute(
                "SELECT first_name, last_name FROM members"
            )
            for row in cursor:
                first, last = row
                if first and last:
                    names.append(f"{first.strip()} {last.strip()}")
        finally:
            conn.close()
    except (sqlite3.Error, OSError):
        pass
    return names


def load_member_names_from_external(data_dir):
    """Load member names from external sources in priority order.

    Checks, in order:
      1. data/members.db  — SQLite with members/attendees table, name/full_name column
      2. data/members.csv — CSV with name/full_name column
      3. config/members.txt — plain text, one name per line

    Args:
        data_dir: Base directory to search within.

    Returns:
        List of name strings, or empty list if nothing found.
    """
    # 1. SQLite database
    db_path = os.path.join(data_dir, "data", "members.db")
    if os.path.isfile(db_path):
        names = _load_from_sqlite(db_path)
        if names:
            return names

    # 2. CSV file
    csv_path = os.path.join(data_dir, "data", "members.csv")
    if os.path.isfile(csv_path):
        names = _load_from_csv(csv_path)
        if names:
            return names

    # 3. Plain text file
    txt_path = os.path.join(data_dir, "config", "members.txt")
    if os.path.isfile(txt_path):
        names = _load_from_txt(txt_path)
        if names:
            return names

    return []



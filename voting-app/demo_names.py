_PARTICLE_PATTERN = re.compile(
    r"^(?:van der|van den|van de|van|de|ten|ter)\s+",
    re.IGNORECASE,
)

def _extract_surname(full_name):
    """Extract the surname from a full name (everything after the first token).

    Returns the full surname string (including any particle) or None.
    """
    if not full_name:
        return None
    parts = full_name.strip().split()
    if len(parts) < 2:
        return None
    # Everything after the first name token is the surname
    return " ".join(parts[1:])


def _generate_names_from_pool(member_names, count=8):
    """Generate candidate names using real Dutch surnames from a curated pool.

    Surnames already present in the member list are excluded so that demo
    candidates are clearly distinct from real members.  Each selected surname
    is paired with a random Dutch first name.

    Returns a list of unique generated name strings of length `count`.
    """
    # Collect member surnames (case-insensitive) to exclude them
    member_surnames_lower = set()
    for name in member_names:
        surname = _extract_surname(name)
        if surname:
            member_surnames_lower.add(surname.lower())

    # Filter pool to surnames not already in the member list
    available = [
        s for s in DUTCH_SURNAMES if s.lower() not in member_surnames_lower
    ]
    random.shuffle(available)

    generated = []
    seen_surnames = set()

    for surname in available:
        if len(generated) >= count:
            break
        if surname.lower() in seen_surnames:
            continue

        first = random.choice(FIRST_NAMES)
        generated.append(f"{first} {surname}")
        seen_surnames.add(surname.lower())

    return generated


